package com.mrhid6.gameengine.obj;

public class TextureCoordinate {

	private float u;
	private float v;
	private float w;
	public float getU() {
		return u;
	}
	public float getV() {
		return v;
	}
	public float getW() {
		return w;
	}
	public void setU(float u) {
		this.u = u;
	}
	public void setV(float v) {
		this.v = v;
	}
	public void setW(float w) {
		this.w = w;
	}
	
	
}
